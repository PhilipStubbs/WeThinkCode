#include <stdio.h>

int	ft_strcmp(char *s1, char *s2)
{
	int count;

	count = 0;
	while (count == 0)
	{
		if (*s1 < *s2)
		{
			count = 1;
		}
		if (*s1 > *s2)
		{
			count = -1;
		}
		if (*s1 == '\0')
		{
			break ;
		}
		s1++;
		s2++;
	}
	return (count);
}
