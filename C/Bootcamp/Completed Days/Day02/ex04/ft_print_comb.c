void	ft_print_comb(void)
{
	int a;
	int	b;
	int	c;

	a = '0';
	while (a <= '7')
	{
		b = '0';
		while (b <= '8')
		{
			c = '0';
			while (c <= '9')
			{
				write(1, &a, 3);
				write(1, &b, 2);
				write(1, &c, 2);
				write(1, ", ", 3);
				c = c + 1;
			}
			b = b + 1;
		}
		a = a + 1;
	}
}
