char	*ft_rot42(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
		{
			str[i] = (((str[i] - 65) + 16) % 26 + 65);
		}
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			str[i] = (((str[i] - 97) + 16) % 26 + 97);
		}
		else
		{
			str[i] = str[i];
		}
		i++;
	}
	return (str);
}
